CREATE TABLE ROOM_ORDER (
    ORDER_ID            INT             NOT NULL AUTO_INCREMENT,
    MEMBER_ID           INT             NOT NULL,
    EMPLOYEE_ID         INT             ,
    MEMBER_COUPON_ID           INT             ,
    ORDER_STATUS        TINYINT         NOT NULL DEFAULT 0  COMMENT '0=PENDING 1=CONFIRMED 2=COMPLETED 3=CANCELED',
    CHECK_IN_DATE       DATE            NOT NULL,
    CHECK_OUT_DATE      DATE            NOT NULL,
    TOTAL_AMOUNT        INT             NOT NULL,
    DISCOUNT_AMOUNT     INT             NOT NULL,
    PAID_AMOUNT         INT             NOT NULL,
    MERCHANT_TRADE_NO   VARCHAR(50)     ,
    ECPAY_TRADE_NO      VARCHAR(50)     ,
    PAYMENT_METHOD      TINYINT         COMMENT '0:CREDIT_CARD 1:ONLINE_PAYMENT',

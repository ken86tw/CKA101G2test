    CONSTRAINT ROOM_ORDER_ORDER_ID_PK     PRIMARY KEY (ORDER_ID),
    CONSTRAINT ROOM_ORDER_MERCHANT_TRADE_NO_UK UNIQUE KEY (MERCHANT_TRADE_NO),
    CONSTRAINT ROOM_ORDER_MEMBER_ID_FK    FOREIGN KEY (MEMBER_ID)   REFERENCES MEMBERS (MEMBER_ID),
    CONSTRAINT ROOM_ORDER_EMPLOYEE_ID_FK  FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE (EMPLOYEE_ID),
    CONSTRAINT ROOM_ORDER_MEMBER_COUPON_ID_FK    FOREIGN KEY (MEMBER_COUPON_ID)   REFERENCES MEMBER_COUPON (MEMBER_COUPON_ID)
